    /** Creates new form MainFrame */
    public Newton1() {
        initComponents();
        setTitle("Simple JOGL Application");

        renderer=new GLRenderer();
        panel.addGLEventListener(renderer);
        animator = new Animator(panel);

        this.addWindowListener(new WindowAdapter() {

            @Override
            public void windowClosing(WindowEvent e) {
                // Run this on another thread than the AWT event queue to
                // make sure the call to Animator.stop() completes before
                // exiting
                new Thread(new Runnable() {

                    public void run() {
                        animator.stop();
                        System.exit(0);
                    }
                }).start();
            }
        });

        this.addKeyListener(new KeyAdapter() {

            @Override
            public void keyPressed(KeyEvent evt) {
                
                if (evt.getKeyCode()==KeyEvent.VK_LEFT){
                    renderer.moverCamara(1);
                }else if (evt.getKeyCode()==KeyEvent.VK_RIGHT){
                    renderer.moverCamara(2);
                }else if (evt.getKeyCode()==KeyEvent.VK_UP){
                    renderer.moverCamara(3);
                }else if (evt.getKeyCode()==KeyEvent.VK_DOWN){
                    renderer.moverCamara(4);
                }else if (evt.getKeyCode()==KeyEvent.VK_PLUS){
                    renderer.moverCamara(5);
                }else if (evt.getKeyCode()==KeyEvent.VK_MINUS){
                    renderer.moverCamara(6);
                }
            }
        });

        animator.start();
    }

    @Override
    public void setVisible(boolean show){
        if(!show)
            animator.stop();
        super.setVisible(show);
        if(!show)
            animator.start();
    }

